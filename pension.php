        <h1>La pension</h1>
      </div>
      <p>Située à Crisan, en bordure du fleuve au numéro 422 de l’unique rue du village, notre pension bénéficie d’une position centrale.<br>
        L’embarcadère principale et la superette sont à moins  de 400 mètres. <br>
        Une longue digue derrière la maison permet une jolie balade à pied offrant un aperçu de la vie traditionnelle à la campagne.</p>
      <h1>Structure de la chambre d’hôte</h1>
      <p>La pension se compose de 8 chambres doubles avec salle de bain, d’une grande salle à manger et d’une belle cour ombragée et fleurie.<br>
        -	4 chambres avec lit matrimonial<br>
        -	4 chambres avec 2 lits simples<br>
        Toutes nos chambres sont équipées d’un ventilateur, du chauffage central, d’une télévision et d’un accès à internet.</p>
      <h1>La table d’hôte</h1>
      <p>Pour notre table d’hôte, nous proposons une cuisine familiale et traditionnelle à base de produits frais.<br>
        Le petit déjeuner est diversifié est copieux : confiture, laitage, œuf, plateau mixte, café ou thé à volonté.<br>
        Le déjeuner est servi sous la forme d’un piquenique surprise et bucolique. Ce qui permet aux voyageurs de partir à la journée explorer le delta soit en bateau soit en canoé.<br>
        Le dîner se compose d’une entrée, d’un plat et d’un dessert. Le poisson est à l’honneur mais pas seulement… Amis végétariens vous êtes les bienvenus !</p>

